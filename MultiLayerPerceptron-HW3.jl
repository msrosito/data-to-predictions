Second connection:

$\text{output}^{(2)} = \sigma.\big(W^{(2)} \text{output}^{(1)} + b^{(2)}\big) = \sigma.\bigg( W^{(2)} \Big(\sigma.\Big(W^{(1)} \begin{pmatrix}
												x_1 \\
