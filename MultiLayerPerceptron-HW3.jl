# ╔═╡ 0e273f66-2619-49e2-ace9-896c87f0b8b3
md"""
Data generation

Input (or independent variables or features) : X, Y

Output (or dependent variable or response) : Z

"""

# ╔═╡ 446d355b-51e4-484d-a8b1-3616c8401944
begin
	g(x, y) = exp(-(x^2 + y^2)/2) # Underlying function
	
	xr = [-1:0.1:1;] # range for x
	yr = [-1:0.1:1;] # range for y

	# a grid is used to evaluate g in each possible pair	
	X = [x for x in xr for y in yr]
	Y = [y for x in xr for y in yr]
	Z = g.(X,Y) .+ 0.01 * randn(1)[1]

	println("About the grid: ")
	println("Lenght of xr and yr: ", length(xr))
	println("Lenght of X: ", length(X)) ## check length(xr) * length(y2) = length(X) 
	println("Lenght of Y: ", length(Y)) ## check length(xr) * length(y2) = length(Y) 
	println("Lenght of Z: ", length(Z)) ## check length(xr) * length(y2) = length(Z)

end

