# ╔═╡ db2c186b-4e45-4f92-963f-85265f0a80f7
md"""
Model definition: To fit the data, we are going to use a MLP. 

About this NN:

- The input layer consists of two elements (features)
- The first hidden layer has three neurons
- The second hidden layer has two neurons
- The output layer has only one element (response)
- Activation function: sigmoid (σ)

"""

# ╔═╡ 5cbde69c-2978-455b-b123-f4930f33cd86
md"""
Training set
"""

# ╔═╡ 57139526-e443-4d15-8fef-65bdd27b0c6f
begin
	# reorganization of data
	x_train = [[x, y] for (x,y) in zip(Float32.(X), Float32.(Y))] # the input should be a 2d vector
	y_train = Float32.(Z)
	data = [(x_train, y_train)]
	
	# zip example: a = [1,2,3], b = [4,5,6,7], 
	# for i in zip(a,b) print(i)
	# (1,4)(2,5)(3,6)
	
	println("x_train is a vector of  " , length(x_train), " bidimensional vectors")

	println("y_train is a vector of  " , length(y_train), " elements")

end

# ╔═╡ 19ecbee6-ab4e-4ff8-aebc-7110245afb02
md"""
Definition of the MLP
"""

# ╔═╡ 4636fa92-dc34-46a9-ada3-c648cc758004
begin
	# building the model
	insize, outsize = 2, 1
	model = Chain(Dense(insize, 3, σ),
				# first connection: 2 inputs, 3 outputs
	              Dense(3, 2, σ),
				# second connection: 3 inupts, 2 outputs
	              Dense(2, outsize, σ))
				# third connection: 2 inputs, 1 output
end

# ╔═╡ 11994771-76df-4952-a5c7-a1b12980febe
model([-2, 2]) # model can be evaluated in an input data point 
			   # the result is a vector of one element (output)

# ╔═╡ 883d111b-2510-4274-857f-d139068cede0
md"""
Training:

- Loss function: mse 
- Optimizer: gradient descent
- Number of epochs: 700

"""

# ╔═╡ 60669985-9680-4db6-b387-a7a85d2b65aa
begin 
	# defining loss
	loss(m, x, y) = Flux.mse(first.(m.(x_train)), y) # broadcast operation: m is evaluated in each component of the vector 

	# defining optimizer
	optimizer = Descent(0.2) # 0.2 is the learning rate

	n_epochs = 700
end	

# ╔═╡ ee83cbe3-54d7-42e8-9b53-104e5f608b3c
begin
	# training (pay attention to the syntax)
	
	losses = [] # history of losses
	
	for _ in 1:n_epochs
	    
	    Flux.train!(loss, model, data, optimizer)
	    
	    # Accumulate losses
	    l = loss(model, x_train, y_train)
	    push!(losses, l)
		
	end
end

# ╔═╡ da280f10-3ae2-4999-921a-e1cab044d62a
plot(losses)

