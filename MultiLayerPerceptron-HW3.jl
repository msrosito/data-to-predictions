Third connection (final output):

$\text{output}^{(3)} = \sigma.\big(W^{(3)} \text{output}^{(2)} + b^{(3)}\big) = \sigma.\Bigg(  W^{(3)} \sigma.\bigg( W^{(2)} \Big(\sigma.\Big(W^{(1)} \begin{pmatrix}
