md"""
A discrete variable X has Bernoulli distribution with parameter p takes the value 1 (success) with probability p and 0 (failure) with probability 1-p.

$X \sim Bernoulli(p)$
"""

# ╔═╡ 5f9ff3cb-1cf1-4399-a030-36ddbd9644c3
md"""
The following function generates a Bernoulli variable
"""

# ╔═╡ 9c057549-4c09-441e-9e06-6232a034981a
function generate_bernoulli(p)
    # p: Probability of success (0 <= p <= 1)    
    
	if rand() <= p #rand() returns a random number between 0 and 1
		bernoulli = 1
	else
		bernoulli= 0
	end
    
    return bernoulli
end

# ╔═╡ 838c6d7c-7e8c-41c8-902c-7f4542217bd8
generate_bernoulli(0.4)

# ╔═╡ 53fc635e-c604-4af2-977f-4dbc5441f6d4
md"""
The expected value of a Bernoulli random variable is p.

Let's run the following cell many times.
"""

# ╔═╡ 339b056b-f65f-4d72-b85f-11d397ada7b0
begin
	n = 1000
	p = 0.4
	bernoullis = [generate_bernoulli(p) for _ in 1:n]
	Exp = sum(bernoullis) / n 
end

